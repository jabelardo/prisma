# Catálogo de Condiciones

Las [Condiciones](./03.2.6_Introduccion_a_las_Condiciones.md) son efectos duraderos que alteran las capacidades o el estado de un personaje o Zona. A continuación, se presenta una lista detallada de Condiciones comunes en Prisma, junto con sus efectos y formas de resolución. El DJ siempre puede crear Condiciones nuevas o modificar estas para ajustarse a la narrativa.

---

### Agonía (Combate)

El personaje sufre un dolor insoportable por una herida, posiblemente infligida por un arma diseñada más para torturar que para matar.
* **Efecto:** Todas las acciones sufren un **aumento de +1 en su [Dificultad](./../../Capitulo_01_Mecanicas_Fundamentales/01.06_Dificultad.md)** durante el resto de la escena.
* **Resolución:** El personaje recibe primeros auxilios (una acción exitosa de Medicina) o se abstiene de realizar acciones físicas durante el resto de la escena.

### Ardiendo

El personaje está en llamas, ya sea por estar en una [Zona](./03.2.2_Distancias_Zonas_y_Areas.md) [En Llamas](./03.2.4_Efectos_de_Area_y_del_Entorno.md) o por una fuente sobrenatural.
* **Efecto:** El personaje sufre **1 herida** (ver Capítulo X: Salud y Daño) al final de su turno hasta que esta Condición se resuelva.
* **Resolución:** El personaje gasta su [Acción Simple](./03.2.3_Tipos_de_Acciones_y_Tareas.md) en su turno para apagarse, o alguien más gasta su acción para hacerlo.

### Aturdido (Combate)

El personaje ha sufrido un shock que lo deja con una fuerte sensación de desorientación.
* **Efecto:** El personaje no puede realizar más de **una [Acción Simple](./03.2.3_Tipos_de_Acciones_y_Tareas.md)** o **dos [Acciones Reflexivas](./03.2.3_Tipos_de_Acciones_y_Tareas.md)** cada ronda hasta que esta Condición se resuelva.
* **Resolución:** La Condición desaparece con el tiempo: 2 rondas si fue causada por una Complicación Menor, 3 si fue Moderada y 4 si fue Mayor. Se resuelve antes si el personaje recibe primeros auxilios.

### Aterrado (Social/Combate)

Tu personaje está tan asustado de alguien o algo que no puede evitar intentar huir.
* **Efecto:** El personaje sufre una **[Complicación](./../../Capitulo_01_Mecanicas_Fundamentales/01.07_Complicaciones.md) Moderada (2)** en cualquier acción que intente que no implique huir del objeto de su terror. Si no supera esta Complicación, debe gastar su siguiente acción realizando un movimiento completo para alejarse de la fuente de su miedo.
* **Resolución:** El personaje se aleja más allá de la [Banda de Distancia](./03.2.2_Distancias_Zonas_y_Areas.md) Larga de la fuente durante más de una ronda, o la escena termina.

### Confundido (Social)

Algo ha desconcertado al personaje, dejándolo incapaz de procesar su entorno lo suficientemente rápido. Su tiempo de reacción disminuye y no puede seguir el ritmo de la acción.
* **Efecto:** El personaje sufre **+1 de Dificultad** en acciones que requieran pensamiento o agudeza mental (como presentar un argumento, buscar pistas, hacer un disparo preciso o usar poderes). Esto reduce la [Complicación](./../../Capitulo_01_Mecanicas_Fundamentales/01.07_Complicaciones.md) de cualquier Regla Especial de antagonista en un paso, hasta un mínimo de Menor (1).
* **Resolución:** El personaje debe gastar su turno reorientándose para quitarse el efecto Confundido. De lo contrario, esta Condición se resuelve al final de la escena.

### Culpable (Social)

Tu personaje se siente culpable por sus acciones y cree que debe expiar sus fechorías.
* **Efecto:** Cada vez que tu personaje realiza una acción que no está destinada a expiar, sufre una **Complicación Menor (1)**. Si no supera la Complicación, su actitud hacia la persona o institución por la que se siente culpable se vuelve más positiva. Si la actitud ya es positiva, forma un nuevo [Vínculo](./../../Capitulo_02_Creacion_y_Progresion_de_Personajes/02.3.3_Camino_de_Vinculo.md) de simpatía de 1 punto hacia el personaje o aumenta un Vínculo existente en 1 paso.
* **Resolución:** El personaje supera la Complicación, o la escena termina.

### Desanimado (Social)

Debido a una tristeza extrema, malestar, culpa o miedo, el personaje está socialmente abrumado.
* **Efecto:** Todas las acciones sociales *contra* el personaje ganan **+1 [Filo](./../../Capitulo_01_Mecanicas_Fundamentales/01.05_Filo.md)** (o Ventaja Social, si se define esa mecánica).
* **Resolución:** Esta Condición termina cuando el personaje gana la Condición "Reanimado".

### Deshonrado (Social)

La reputación del personaje es arrastrada por el barro porque le falló a su gente o a sus ideales de manera significativa.
* **Efecto:** Todos los que presenciaron el fallo cambian su actitud un paso negativo hacia el personaje. Si su actitud ya era negativa, esto solo refuerza su mala opinión.
* **Resolución:** El personaje debe invertir tiempo y acciones significativas (posiblemente una [Acción Compleja](./03.2.3_Tipos_de_Acciones_y_Tareas.md)) para restaurar su reputación.

### Exhausto

El personaje ha estado despierto demasiado tiempo y se está agotando.
* **Efecto:** El personaje sufre una **Complicación Moderada (2)** en todas las acciones hasta que se vaya a dormir. No superar la Complicación resulta en que el personaje alucina, experimenta pérdida de memoria a corto plazo o se ve afectado por otra Condición apropiada.
* **Resolución:** El personaje descansa durante ocho horas o tres escenas.

### Exposición

El personaje ha estado expuesto a calor extremo, frío extremo o un extremo sobrenatural que lo ha dejado luchando por su vida.
* **Efecto:** Todas las [Complicaciones](./../../Capitulo_01_Mecanicas_Fundamentales/01.07_Complicaciones.md) cuestan **un Acierto más** para superarlas (Menor = 2, Moderada = 3, Mayor = 4).
* **Resolución:** El personaje recibe primeros auxilios, o el efecto se desvanece después de dos escenas de abandonar la Zona donde ganó la Condición.

### Favor Inesperado (Social)

Tu personaje debe un favor que debe completarse en el futuro. La naturaleza del favor la determina el jugador, pero debe ser algo que el personaje sea reacio a hacer (llevar un mensaje infeliz, entrenar a la mascota de alguien, etc.).
* **Efecto:** Hasta que tu personaje realice el favor, la persona a la que se lo debe tiene **+1 [Filo](./../../Capitulo_01_Mecanicas_Fundamentales/01.05_Filo.md)** en las interacciones sociales con él.
* **Resolución:** El personaje realiza la tarea.

### Fuera de Combate (Combate)

El personaje ha sido derrotado, ya sea por daño o por elección propia para evitarlo.
* **Efecto:** El personaje no puede realizar más acciones y es retirado del enfrentamiento.
* **Resolución:** La escena de combate termina, o el personaje se beneficia de una acción apropiada para recuperarlo (primeros auxilios, etc.).

### Gaseoso

El cuerpo de un personaje gaseoso se ha convertido en humo, vapor o una nube de niebla.
* **Efecto:** Mientras está en este estado, tiene una **[Escala](./03.2.5_Escalas_de_Poder.md) de +2** para Durabilidad contra ataques entrantes. El personaje no puede iniciar ataques que no sean de Combate Cercano. Al usar Combate Cercano, solo puede comprar [Tretas](./../../Capitulo_01_Mecanicas_Fundamentales/01.08_Tretas.md) de Combate pero no puede infligir daño. Cuando compra una Treta de Combate, vuelve a ser tangible. Una criatura gaseosa puede moverse a través de huecos tan pequeños como 1 milímetro, a su velocidad normal.
* **Resolución:** Si el personaje eligió volverse gaseoso, dura lo que diga el poder o hasta que elija terminarlo (acción de Supervivencia o Esotérica, Dificultad 2). Si fue forzado, dura 1 + Aciertos extra de la acción que lo causó. Restaurarlo requiere Medicina или Esotérica (Dificultad 3).

### Herida Agravada (Combate)

El personaje tiene una herida grave que lo pone al borde de la muerte.
* **Efecto:** Si el personaje falla una acción mientras está bajo el efecto de esta Condición, cae inmediatamente a "Cerca de la Muerte" (o equivalente en Prisma), sin importar cuántos Niveles de herida le queden. Los antagonistas caen a 1 nivel de salud. * **Resolución:** El personaje recibe primeros auxilios, usa un poder apropiado o cae a "Cerca de la Muerte".

### Inmovilizado (Combate)

El personaje está atrapado y no puede moverse. Puede ser por estar atado, herido o atrapado en barro.
* **Efecto:** No puede realizar acciones de movimiento físico (la teletransportación está permitida).
* **Resolución:** El personaje gasta una acción para liberarse.

### Infectado

El personaje lucha contra una infección, ya sea mística o biológica, que lo devora desde dentro.
* **Efecto:** La infección comienza en 1. Cada ronda posterior, el objetivo debe superar una **Complicación Moderada (2)** en cualquier acción física o mental. No superarla aumenta la calificación de la infección en 1. Si llega a 5, el objetivo gana la Condición "Fuera de Combate" o similar.
* **Resolución:** El personaje recibe ayuda médica u oculta.

### Insensible (Combate)

El personaje ha sido golpeado por un arma o drogado con algo anestésico. Aunque el daño sigue ahí, no puede sentirlo (y puede que no se dé cuenta).
* **Efecto:** No gana beneficios por niveles de herida bajos (si aplica), el dolor no lo distrae ni obliga. No puede ganar ni mantener la Condición "Agonía".
* **Resolución:** El personaje recibe primeros auxilios, o la escena termina.

### Inspirado

El personaje ha sido inspirado a actuar por otro.
* **Efecto:** Gana **+3 Aciertos automáticos** al realizar la acción que el personaje inspirador dictó. * **Resolución:** Esta Condición se resuelve después de que el personaje realiza la acción dictada.

### Lento

Algo ha hecho que el personaje se mueva a un ritmo reducido (agua, nieve, drogas, peso).
* **Efecto:** Debe usar una [Acción Simple](./03.2.3_Tipos_de_Acciones_y_Tareas.md) para moverse y no puede correr o esprintar.
* **Resolución:** El personaje abandona la zona que causa el efecto, o la magia que lo aplicó se desvanece.

### Licuado

El cuerpo de un personaje licuado se ha convertido en un fluido viscoso.
* **Efecto:** Mientras está en este estado, tiene una **[Escala](./03.2.5_Escalas_de_Poder.md) de +2** para Durabilidad contra ataques entrantes. El personaje no puede iniciar ataques que no sean de Combate Cercano. Al usar Combate Cercano, solo puede comprar la Treta "Derribar". Incluso las Tretas Universales le están negadas. Si compra "Derribar", el objetivo cae sobre él (pudiendo causar efectos adicionales). Se mueve a través de huecos de 1 mm a velocidad normal. No puede ser "Sujetado".
* **Resolución:** Similar a "Gaseoso".

### Mentiroso (Social)

Te han pillado en una mentira.
* **Efecto:** Elige una: el objetivo guarda rencor por la mentira y crea un [Vínculo](./../../Capitulo_02_Creacion_y_Progresion_de_Personajes/02.3.3_Camino_de_Vinculo.md) de animosidad de 1 punto, o lo acepta esta vez pero gana **+1 [Filo](./../../Capitulo_01_Mecanicas_Fundamentales/01.05_Filo.md)** para resistir tus intentos de mentirle durante el resto de la escena.
* **Resolución:** Solo el tiempo o la verdad restaurarán su fe en el personaje.

### Petrificado

El personaje ha sido congelado en piedra. Dura 1 + Aciertos extra de la acción que lo causó.
* **Efecto:** Sufre una **Complicación Mayor (3)** en todas las acciones. Si no la supera, sus Atributos Físicos se reducen en 1 cada ronda. Si todos llegan a 0, se convierte en una estatua inanimada.
* **Resolución:** Superar con Supervivencia o Esotérica (Dificultad 2), o el tiempo pasa. Restaurarlo requiere Medicina o Esotérica (Dificultad 3).

### Privado

El personaje no ha comido ni bebido nada sustancial en mucho tiempo.
* **Efecto:** Todas las acciones sufren **+1 de Dificultad**.
* **Resolución:** El personaje come o bebe algo.

### Reanimado

El personaje ha descansado o comido y bebido, listo para enfrentarse al mundo.
* **Efecto:** Gana **+1 Acierto automático** en todas las acciones hasta que esta Condición termine. * **Resolución:** Termina después de la tercera acción del personaje o después de la primera sesión de aventura, lo que ocurra primero.

### Sangrando (Combate)

El personaje está perdiendo sangre por una herida o hemorragia interna.
* **Efecto:** Todas las acciones Físicas conllevan una **Complicación Menor (1)** que, si no se supera, inflige 1 herida.
* **Resolución:** El personaje recibe primeros auxilios o cae a "Cerca de la Muerte".

### Sentidos Ocluidos

Los sentidos del personaje han sido oscurecidos por sombras o drogas alucinógenas. Todos sus sentidos están embotados.
* **Efecto:** Cualquier acción que requiera sus sentidos (comunicación, defensa, ataque, equilibrio) sufre una **Complicación** (Menor, Moderada o Mayor, según la causa). Si no se supera, se arriesga a sufrir heridas, comunicar mal, perder información, caer o atacar a la persona equivocada.
* **Resolución:** Se desvanece con el tiempo (2 rondas para Menor, 3 para Moderada, 4 para Mayor).

### Sujetado (Combate)

El personaje está atrapado en un agarre y debe actuar para maniobrar.
* **Efecto:** No puede realizar acciones de Defensa contra su atacante (ni siquiera reflexivas). Puede comprar las siguientes Tretas en ataques contra su oponente:
    * **Abrumar (2 Aciertos):** Gana el control del agarre, resolviendo esta Condición y aplicándosela a su oponente.
    * **Liberarse (1 Acierto):** Abandona el agarre, resolviendo esta Condición.
* **Resolución:** Compra una de las Tretas anteriores, o alguien más rompe el agarre.

### Toxina de Acción Lenta

El personaje ha encontrado una toxina de acción lenta (mordedura venenosa, comida en mal estado).
* **Efecto:** En la escena del encuentro, sufre una **Complicación Menor (1)** en acciones físicas. Si no se resuelve, aumenta a **Complicación Mayor (3)** y se aplica a *todas* las acciones. No superarla puede causar 1 herida, parálisis de miembros, parálisis total o delirio.
* **Resolución:** En la primera escena, si se elimina la toxina o se administra un antídoto. De lo contrario, se resuelve después de superar la Complicación en dos acciones posteriores o al final de la sesión de juego (a discreción del DJ).

### Toxina de Acción Rápida

El personaje ha encontrado una toxina por ingestión, inyección o contacto. Dura 1 + Aciertos extra de la acción que lo infligió.
* **Efecto:** Inflige **1 herida** por ronda. Algunas toxinas pueden causar otros efectos (sueño, parálisis) en lugar de daño.
* **Resolución:** Superar con Supervivencia (acción simple), o el tiempo pasa.